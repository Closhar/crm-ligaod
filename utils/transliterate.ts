/**
 * Функция транслитерации русского текста в латиницу для создания URL-совместимых строк
 * 
 * @param text - Исходный текст для транслитерации
 * @returns Транслитерированный текст, пригодный для использования в URL
 */
export function transliterate(text: string): string {
  if (!text) return '';
  
  const rusToLat: Record<string, string> = {
    'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'e',
    'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
    'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
    'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sch', 'ъ': '',
    'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
    'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'E',
    'Ж': 'Zh', 'З': 'Z', 'И': 'I', 'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M',
    'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T', 'У': 'U',
    'Ф': 'F', 'Х': 'H', 'Ц': 'Ts', 'Ч': 'Ch', 'Ш': 'Sh', 'Щ': 'Sch', 'Ъ': '',
    'Ы': 'Y', 'Ь': '', 'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya',
    ' ': '-', '_': '-', ',': '', '.': '', '?': '', '!': '', '«': '', '»': '',
    '(': '', ')': '', '[': '', ']': '', '{': '', '}': '', '\'': '', '"': '',
    '+': '', '=': '', '&': '', ':': '', ';': '', '*': '', '%': '', '/': '-', 
    '\\': '-', '$': '', '@': '', '#': '', '^': '', '`': '', '|': '', '<': '', '>': ''
  };
  
  // Преобразуем текст
  const result = text.split('').map(char => {
    return rusToLat[char] !== undefined ? rusToLat[char] : char;
  }).join('');
  
  // Приводим к нижнему регистру и обрабатываем специальные случаи
  return result
    .toLowerCase()
    .replace(/-+/g, '-')       // Заменяем множественные дефисы на один
    .replace(/^-|-$/g, '')     // Убираем дефисы в начале и конце
    .replace(/[^a-z0-9-]/g, ''); // Оставляем только латинские буквы, цифры и дефисы
} 